package sensos.contracts.messaging.v1;

import java.io.Serializable;

/**
 *
 * @author sensos
 */
public class JsonConfigurationResponseConfigureElement  implements Serializable {

    String key;
    String value;

    public JsonConfigurationResponseConfigureElement() {

    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

}